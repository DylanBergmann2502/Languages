
from requests_html import HTML

with open('simple_requests_html.html') as html_file:
    source = html_file.read()
    html = HTML(html=source)
    html.render() # get the text generated by javascript, hard to do with bs4

print(html.html) # quite like prettify()
print(html.text) # get text without the tag

match1 = html.find('title')
print(match1) # print out list of ELements
print(match1[0].html) # match1 = html.find('title', first = True) => set first = True
print(match1[0].text)

# hashtag# to find id
match2 = html.find('#footer', first=True)
print(match2.text)

# article = html.find('div.article', first=True) # div with class of 'article'
articles = html.find('div.article') # findall()

for article in articles:
    headline = article.find('h2', first=True).text
    print(headline)

    description = article.find('p', first=True).text
    print(description)

    print()

# get javascript text
match3 = html.find('#footer', first=True)
print(match3.html)
